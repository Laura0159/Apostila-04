public class Impressora{
    public void exibir (float ex1){
        System.out.println(ex1);
    }
    public void exibir (float a, float b){
        System.out.println(a+"," +b);
    }
    public void exibir (float c, String t1){
        System.out.println(c+"," +t1);
    }
    public void exibir (String t2, float d){
        System.out.println(t2+"," +d);
    }
    public void exibir (String t3, String t4, String t5){
        System.out.println(t3+"," +t4+ "," +t5);
    }
    public void exibir (int x, int y, int z ){
        System.out.println(x+"," +y+"," +z);
    }
}